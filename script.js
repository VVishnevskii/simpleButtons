var buttonPageHeader_1 = document.querySelector('.button_1');

buttonPageHeader_1.addEventListener('click', function() {
  var buttonStatus_1 = document.querySelector('.active_1');
    if (buttonStatus_1) {
      buttonPageHeader_1.classList.remove('active_1');
    }
  else {
    buttonPageHeader_1.classList.add('active_1');
  }
})

